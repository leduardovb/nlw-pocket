export function translatedMonth(month: string) {
  switch (month) {
    case 'January':
      return 'Janeiro'
    case 'February':
      return 'Fevereiro'
    case 'March':
      return 'Mar√ßo'
    case 'April':
      return 'Abril'
    case 'May':
      return 'Maio'
    case 'June':
      return 'Junho'
    case 'July':
      return 'Julho'
    case 'August':
      return 'Agosto'
    case 'September':
      return 'Setembro'
    case 'October':
      return 'Outubro'
    case 'November':
      return 'Novembro'
    case 'December':
      return 'Dezembro'
  }
}
